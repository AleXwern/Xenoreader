#include "libft.h"

size_t		ft_intsize(long n)
{
	int		size;

	size = 0;
	if (n < 0)
	{
		size++;
		n = n * -1;
	}
	while (n > 9)
	{
		size++;
		n /= 10;
	}
	return (size++);
}
